package com.aspose.cloud.slides;

public class Placeholder {
	private UriResponse SelfUri;
	private int Index;
	private Orientation Orientation;
	private Size Size;
	private Type Type;
	private ShapeURI Shape;

	public UriResponse getSelfUri() {
		return SelfUri;
	}

	public void setSelfUri(UriResponse selfUri) {
		SelfUri = selfUri;
	}

	public int getIndex() {
		return Index;
	}

	public void setIndex(int index) {
		Index = index;
	}

	public Orientation getOrientation() {
		return Orientation;
	}

	public void setOrientation(Orientation orientation) {
		Orientation = orientation;
	}

	public Size getSize() {
		return Size;
	}

	public void setSize(Size size) {
		Size = size;
	}

	public Type getType() {
		return Type;
	}

	public void setType(Type type) {
		Type = type;
	}

	public ShapeURI getShape() {
		return Shape;
	}

	public void setShape(ShapeURI shape) {
		Shape = shape;
	}
}
